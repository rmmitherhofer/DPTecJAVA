package dptec.model;

public class BancoModel {
    private int cdbanco;
    private String nmbanco;
    private String sgbanco;
    private String dscompetencia;
    private String dswebsite;
    private int icativo;

    public int getCdbanco() {
        return cdbanco;
    }

    public void setCdbanco(int cdbanco) {
        this.cdbanco = cdbanco;
    }

    public String getNmbanco() {
        return nmbanco;
    }

    public void setNmbanco(String nmbanco) {
        this.nmbanco = nmbanco;
    }

    public String getSgbanco() {
        return sgbanco;
    }

    public void setSgbanco(String sgbanco) {
        this.sgbanco = sgbanco;
    }

    public String getDscompetencia() {
        return dscompetencia;
    }

    public void setDscompetencia(String dscompetencia) {
        this.dscompetencia = dscompetencia;
    }

    public String getDswebsite() {
        return dswebsite;
    }

    public void setDswebsite(String dswebsite) {
        this.dswebsite = dswebsite;
    }

    public int getIcativo() {
        return icativo;
    }

    public void setIcativo(int icativo) {
        this.icativo = icativo;
    }
}
