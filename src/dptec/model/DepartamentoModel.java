public class DepartamentoModel {
    private int cddepartamento;
    private String dsdepartamento;
    private String cdccusto;
    private int cdfuncionario;
    private int icativo;

    public int getCddepartamento() {
        return cddepartamento;
    }

    public void setCddepartamento(int cddepartamento) {
        this.cddepartamento = cddepartamento;
    }

    public String getDsdepartamento() {
        return dsdepartamento;
    }

    public void setDsdepartamento(String dsdepartamento) {
        this.dsdepartamento = dsdepartamento;
    }

    public String getCdccusto() {
        return cdccusto;
    }

    public void setCdccusto(String cdccusto) {
        this.cdccusto = cdccusto;
    }

    public int getCdfuncionario() {
        return cdfuncionario;
    }

    public void setCdfuncionario(int cdfuncionario) {
        this.cdfuncionario = cdfuncionario;
    }

    public int getIcativo() {
        return icativo;
    }

    public void setIcativo(int icativo) {
        this.icativo = icativo;
    }
    
}
